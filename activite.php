<div class="banner"><h1>Activités</h1></div>
<span class="caracter"> 
<p>L'Association des Mamans Chrétienne pour l'Assistance Aux Vulnérables est un ONG qui fait quatre activités dans les domaines suivants:
	<div class="banner"><h4>Genre VIH/SIDA</h4></div>
Dans ce domaine, AMCAV aide des femmes et enfants qui ont été victimés de la violence sexuelle et ainsi que de la maladie du VIH/SIDA.

Les Enfants et Leurs Parents qui sont Victim&eacute;s du VIH/SIDA recoivent quelques Habits, des Souliers, des Assiettes, et bien d'autres biens financés par Leurs Bailleurs de Fonds r&eacute;sidant dans diff&eacute;rents Pays.
Ces enfants poss&egrave;dent leur &eacute;cole pour une bonne apprentissage, la section Primaire et Secondaire sont leur disposition pour recevoir une bonne &eacute;ducation de la part de ces enseignants.	
	<br/><br/><div class="banner"><h4>Information et Formation</h4></div>
Dans ce domaine de la formation, AMCAV fait de formations de différents membres pour bien gérer les femmes et enfants qui ont été victimés du VIH/SIDA, les agents cette ONG recoivent aussi 
de formations dans le domaine de la psychologie pour le renforcement de leur capacité.<br/>
En ce qui concerne l'Information, la coordinatrice de l'AMCAV Maman Bernadette Ntumba fait passer une émission à la Radio Télévision Nationale Congolaise pour une grande éclaircissement du message 
de femmes et enfants victimes de violences sexuelles dans la plaine de la Ruzizi ainsi qu'à Fizi.

	<br/><br/><div class="banner"><h4>Protection et Droits Humains</h4></div>
AMCAV possède pour une cabinet juridique un don de L'ABA ROLI envue de combattre les violences sexuelles faites aux femmes et enfants dans la plaine de la Ruzizi ainsi qu'à Fizi.
C'est pourquoi cet ONG travaille avec le tribunal des grands instances pour bien juger les malfaiteurs.<br/><br/>
	
	<div class="banner"><h4>Agro-Pastorale</h4></div>
L'Association des Mamans Chr&eacute;tiennes pour l'Assistance aux Vuln&eacute;rables, est une ONG qui travaille avec des femmes dans les domaines de L'agriculture et des &eacute;l&egrave;vages,
des champs communautaires aident des femmes qui ont &eacute;t&eacute; victim&eacute;es des violences sexuelles. l'agriculture et l'&eacute;l&egrave;vage permettent aux femmes et aux enfants vitimes du VIH/SIDA d'avoir une meilleure vie et bonne sant&eacute; en qui concerne 
l'alimentation.<br/><br/>
	
