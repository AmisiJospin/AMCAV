<span class="caracter"> 
Nous sommes une organisation locale de droit congolais dénommée <strong>«ASSOCIATION DES MAMANS CHRETIENNES POUR L’ASSISTANCE AUX VULNERABLES» «AMCAV»</strong> en sigle, <strong>créée en 1999 à Uvira</strong>, Territoire d'Uvira, Province du Sud-kivu, Est de la République Démocratique du Congo, conformément à la loi <strong>N°004/2001 du 20 Juillet 2001</strong> portant disposition générales applicables aux Associations Sans But Lucratif et aux Etablissements d’utilité publique en République démocratique du Congo.
Nous fonctionnons sous l’acte de reconnaissance <strong>n°JUST.GS.112/S-KV/102/2001 du 12/01/2001</strong>  du Chef de Division Provinciale de la Justice et Garde des sceaux du Sud-Kivu.<br/>
Cette association avait étè créée pour renforcer la contribution des autres organisations de la société civile  nationale et internationale qui œuvrent dans la promotion et la défense des droits humains des femmes  et des enfants  au Sud-Kivu, Est de la République Démocratique du Congo.
